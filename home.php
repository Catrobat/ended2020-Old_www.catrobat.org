    <div class="two-split">
      <p class="no-break">Free tools enable teenagers to take the leap from being a passive user to become a creative developer. With apps like Pocket Code, users get the chance to use their mobile devices in a meaningful and, most importantly, fun way, without the need of being a professional developer. The visual &ldquo;Lego-Style&rdquo; programming framework, which is used in our projects, enables everyone to design their very own apps, without any limitations such as there are in a common programming language. With the right tools, creating apps, games and animations can be as simple as anything! It can be done by anyone, anytime and everywhere.</p>
