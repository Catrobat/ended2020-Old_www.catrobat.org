    <h1><img class="header-icon" src="img/icon_mission.png" />Our Mission</h1>
    <p>The nonprofit Catrobat project aims at developing solutions which inspire teenagers and kids to learn coding, get creative and be prepared for the digital future of our world. Simple logical processes, creative solutions and easily understandable connections are essential in the world of tomorrow. Our vision is to provide young people with the chance to include these principles in their everyday digital-life. We believe that free, age-based and motivating software, which can be used directly by teenagers, or in education, is essential in order to prepare the next generation for their future.</p>
