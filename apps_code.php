  <h2>What is Pocket Code?</h2>
  <p>With Pocket Code creating a very own app is easier than ever before. New programs, games, animations or stories can be developed within minutes by simply sticking together bricks like Lego. The bricks are easy to understand and are used instead of boring code, so that no previous knowledge is needed. Thus it is the ideal app for children and teenagers to get creative and let their ideas become true. For more experienced programmers all elements known from common programming languages, such as if-else statements, loops or variables are available and by including the phone's sensors and mathematical functions it is possible to realize new individual apps on all mobile devices anytime and anywhere.</p>
  <p>Pocket Code is available for free on Google Play.<br />Download it now!</p>
