  <h2>What is Pocket Paint?</h2>
  <p>Pocket Paint gives our users the opportunity to make their very own Pocket Code apps more colorful and individual. They can make use of features such as drawing graphics using  geometric figures, editing  images with stamps, brushes or transparency and many more, to realize their graphical ideas. The results can be imported directly into Pocket Code where they can be included in any project as a character or background. We want to make it possible for everyone to get creative in their own way and therefore Pocket Paint can also be used as a stand-alone mobile paint program that allows users to save their work directly on any device as png-file. Pocket Paint is the perfect app for anyone who wants to be creative on the go.</p>
  <p>Pocket Paint is available for free on Google Play.<br />Download it now!</p>
